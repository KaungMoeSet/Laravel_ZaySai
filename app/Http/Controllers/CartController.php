    public function addToCart(Request $request)
    {
        $productId = $request->input('product_id');
        $product = Product::find($productId);

        if (!$product) {
            abort(404);
        }

        $cart = new Cart();
        $cart->add($product);
        
        // Save the cart in the session
        $request->session()->put('cart', $cart);

        return redirect()->route('cart.index');
    }
